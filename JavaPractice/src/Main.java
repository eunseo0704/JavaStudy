public class Main {
    public static void main(String[] args) {
        int age = 20; // 나이를 나타내는 변수

        // 나이에 따라 출력
        if (age >= 20) {
            System.out.println("성인입니다.");
        } else if (age >= 14) {
            System.out.println("청소년입니다.");
        } else {
            System.out.println("어린이입니다.");
        }
    }
}
